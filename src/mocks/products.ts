export interface Product {
  id: string;
  /** Human-readable URL slug (e.g. "cjc-1295-ipamorelin-10mg"). Real live
   *  products always have one (see api/products.ts); optional here so mock
   *  data doesn't need updating — mock ids are already slug-shaped, so
   *  components fall back to `product.slug ?? product.id` for links. */
  slug?: string;
  /** Real WooCommerce numeric product id — always present on live data (see
   *  api/products.ts). Needed at checkout: WooCommerce's REST API rejects
   *  an order line item unless it has a real product_id/variation_id or a
   *  matching sku, and the "peptide_code" custom field this storefront used
   *  for that lookup is often blank, silently breaking checkout entirely
   *  (2026-07-24 fix — see checkout/page.tsx). Sending these ids directly
   *  removes that dependency. Optional so mock data still type-checks. */
  wcProductId?: number;
  /** Real WooCommerce numeric variation id — set only for a variable
   *  product's specific dosage variant; null/absent for simple products. */
  wcVariationId?: number | null;
  name: string;
  peptideCode: string;
  casNumber: string;
  category: string;
  subcategory: string;
  purity: string;
  priceMin: number;
  priceMax: number;
  dosage: string;
  description: string;
  rating: number;
  reviewCount: number;
  stockCount: number;
  stockStatus: 'In Stock' | 'Low Stock' | string;
  featured: boolean;
  popular: boolean;
  coaUrl: string;
  testUrl: string;
  image: string;
  /** Per-tier prices sourced from WooCommerce `_tier_price_*` meta.
   *  Optional — mock/demo data and SKUs not yet priced in every tier
   *  simply fall back to priceMin (see src/utils/pricing.ts). */
  tierPrices?: {
    retail?: number;
    wholesale?: number;
    affiliate?: number;
    friends_family?: number;
  };
}

